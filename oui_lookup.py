def lookup_vendor(mac: str) -> str:
    """
    Look up the vendor/manufacturer for a MAC address.
    Returns the vendor name or 'Unknown' if not in the database.
    """
    if not mac or mac.lower() in ("n/a", "ff:ff:ff:ff:ff:ff"):
        return ""
    # Normalize: uppercase, colon-separated
    mac_clean = mac.upper().replace("-", ":").strip()
    prefix = mac_clean[:8]  # first 3 octets = "XX:XX:XX"
    return OUI_DB.get(prefix, "Unknown")
